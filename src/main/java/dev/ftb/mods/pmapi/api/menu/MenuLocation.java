package dev.ftb.mods.pmapi.api.menu;

/**
 * Represents a target location for a pause menu item.
 * <p>
 * MENU_LEFT and MENU_RIGHT are used for the left and right sides of the pause menu.
 */
public enum MenuLocation {
    TOP_LEFT,
    TOP_RIGHT,
    TOP_CENTER,
    BOTTOM_LEFT,
    BOTTOM_RIGHT,
    BOTTOM_CENTER,
    MENU_LEFT,
    MENU_RIGHT,
}
